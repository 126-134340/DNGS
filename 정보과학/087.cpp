#include <stdio.h>
int d[100][100];
int n, m, k;
int x, y, v;
int main()
{
	scanf("%d %d %d", &n, &m, &k);
	
	for(int i=0; i<k; i++)
	{
		scanf("%d %d %d", &x, &y, &v);
		d[x][y]=v;
	}
	
	for(int i=0; i<n; i++)
	{
		for(int j=0; j<m; j++)
			printf("%d ", d[i][j]);
		printf("\n");
	}
}


